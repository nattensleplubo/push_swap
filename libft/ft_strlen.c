#include "libft.h"

size_t	ft_strlen(const char *s)
{
	int	i;
	int	ret;

	i = 0;
	ret = 0;
	while (s[i] == '0' && s[i])
		i++;
	while (s[i])
		i += (ret++, 1);
	return (ret);
}
